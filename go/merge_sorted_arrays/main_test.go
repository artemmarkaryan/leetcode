package main

import (
	"fmt"
	"testing"

	"github.com/stretchr/testify/assert"
)

func Test_merge(t *testing.T) {
	tests := []struct {
		nums1 []int
		n     int
		nums2 []int
		m     int
		want  []int
	}{
		// {
		// 	nums1: []int{1, 2, 0},
		// 	n:     2,
		// 	nums2: []int{3},
		// 	m:     1,
		// 	want:  []int{1, 2, 3},
		// },
		// {
		// 	nums1: []int{1, 3, 0},
		// 	n:     2,
		// 	nums2: []int{2},
		// 	m:     1,
		// 	want:  []int{1, 2, 3},
		// },
		// {
		// 	nums1: []int{4, 5, 6, 0, 0, 0},
		// 	n:     3,
		// 	nums2: []int{1, 2, 3},
		// 	m:     3,
		// 	want:  []int{1, 2, 3, 4, 5, 6},
		// },
		// {
		// 	nums1: []int{1, 2, 4, 5, 6, 0},
		// 	n:     5,
		// 	nums2: []int{3},
		// 	m:     1,
		// 	want:  []int{1, 2, 3, 4, 5, 6},
		// },
		// {
		// 	nums1: []int{-1, 0, 1, 2},
		// 	n:     4,
		// 	nums2: []int{},
		// 	m:     0,
		// 	want:  []int{-1, 0, 1, 2},
		// },
		// {
		// 	nums1: []int{0, 0, 3, 0, 0, 0, 0, 0, 0},
		// 	n:     3,
		// 	nums2: []int{-1, 1, 1, 1, 2, 3},
		// 	m:     6,
		// 	want:  []int{-1, 0, 0, 1, 1, 1, 2, 3, 3},
		// },
		// {
		// 	nums1: []int{0, 0, 0, 3, 0, 0},
		// 	n:     4,
		// 	nums2: []int{1, 2},
		// 	m:     2,
		// 	want:  []int{0, 0, 0, 1, 2, 3},
		// },
		// {
		// 	nums1: []int{-1, 0, 0, 0, 3, 0, 0, 0, 0, 0, 0},
		// 	n:     5,
		// 	nums2: []int{-1, -1, 0, 0, 1, 2},
		// 	m:     6,
		// 	want:  []int{-1, -1, -1, 0, 0, 0, 0, 0, 1, 2, 3},
		// },
		// {
		// 	nums1: []int{0, 0, 3, 0, 0, 0, 0},
		// 	n:     3,
		// 	nums2: []int{-1, -1, 0, 2},
		// 	m:     4,
		// 	want:  []int{-1, -1, 0, 0, 0, 2, 3},
		// },
		{
			nums1: []int{-48, -46, -46, -46, -45, -45, -44, -42, -38, -36, -35, -34, -33, -32, -32, -32, -30, -29, -28, -28, -26, -25, -23, -19, -15, -13, -12, -10, -8, -7, -6, -4, -2, 0, 0, 0, 1, 1, 2, 4, 4, 5, 6, 8, 11, 11, 16, 16, 22, 23, 23, 24, 25, 25, 26, 28, 29, 30, 32, 33, 34, 34, 35, 35, 36, 36, 38, 40, 41, 42, 42, 42, 42, 45, 48, 0, 0, 0, 0, 0, 0, 0},
			n:     75,
			nums2: []int{-36, -27, -24, -14, -13, 2, 9},
			m:     7,
			want:  []int{-48, -46, -46, -46, -45, -45, -44, -42, -38, -36, -36, -35, -34, -33, -32, -32, -32, -30, -29, -28, -28, -27, -26, -25, -24, -23, -19, -15, -14, -13, -13, -12, -10, -8, -7, -6, -4, -2, 0, 0, 0, 1, 1, 2, 2, 4, 4, 5, 6, 8, 9, 11, 11, 16, 16, 22, 23, 23, 24, 25, 25, 26, 28, 29, 30, 32, 33, 34, 34, 35, 35, 36, 36, 38, 40, 41, 42, 42, 42, 42, 45, 48},
		},
	}
	for _, tt := range tests {
		t.Run(fmt.Sprint(tt.nums1, tt.nums2), func(t *testing.T) {
			merge(tt.nums1, tt.n, tt.nums2, tt.m)
			assert.Equal(t, tt.want, tt.nums1)
		})
	}
}
